package refactored;

public class PrintConfiguration {
	final int MAX_NUM_PRIMES = 1000;
	final int ROWS_PER_PAGE = 50;
	final int COLS_PER_PAGE = 4;
    final int ORDMAX = 30;
    
	public int getMAX_NUM_PRIMES() {
		return MAX_NUM_PRIMES;
	}
	public int getROWS_PER_PAGE() {
		return ROWS_PER_PAGE;
	}
	public int getCOLS_PER_PAGE() {
		return COLS_PER_PAGE;
	}
	public int getORDMAX() {
		return ORDMAX;
	} 
}
